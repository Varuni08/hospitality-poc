def _get_reference_values(df):
    food_cuisines   = sorted(df[df["Record_Type"] == "Food"]["Cuisine"].dropna().unique().tolist())
    beverage_types  = sorted(df[df["Record_Type"] == "Alcohol"]["Beverage_Type"].dropna().unique().tolist())
    food_categories = sorted(df[df["Record_Type"] == "Food"]["Category"].dropna().unique().tolist())
