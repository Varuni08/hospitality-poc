def normalise_menu(raw_df):
    #cleaning
    df = raw_df.copy()
    for col in df.select_dtypes(include="object").columns:
        df[col] = df[col].fillna("").astype(str).str.strip()
    for col in ["Record_Type", "Cuisine", "Category", "Dietary_Tags", "Beverage_Type"]:
        if col in df.columns:
            df[col] = df[col].str.title()
    return df

def build_summary_docs(df):
    docs = []

    cuisines      = sorted([c for c in df["Cuisine"].unique() if c and c != "Alcohol"])
    food_cats     = sorted([c for c in df["Category"].unique() if c and c != "Alcoholic Beverage"])
    alcohol_types = sorted([b for b in df["Beverage_Type"].unique() if b])
    dietary_tags  = sorted([t for t in df["Dietary_Tags"].unique() if t])

    master_text = (
        f"Saffron Table Bistro menu overview. "
        f"Available cuisines: {', '.join(cuisines)}. "
        f"Food categories available: {', '.join(food_cats)}. "
        f"Alcohol menu available: Yes. "
        f"Alcohol types available: {', '.join(alcohol_types)}. "
        f"Dietary tags found in dataset: {', '.join(dietary_tags)}."
    )
    docs.append({
        "id": "summary-master",
        "text": master_text,
        "metadata": {"doc_type": "summary", "section": "master_overview"}
    })

    for cuisine in cuisines:
        sub = df[(df["Cuisine"] == cuisine) & (df["Record_Type"] == "Food")]
        cats  = sorted(sub["Category"].unique())
        tags  = sorted(sub["Dietary_Tags"].unique())
        items = sorted(sub["Item_Name"].unique())
        text = (
            f"Cuisine summary for {cuisine}. "
            f"Categories: {', '.join(cats)}. "
            f"Dietary options: {', '.join(tags)}. "
            f"Example dishes: {', '.join(items[:10])}. "
            f"Total items: {len(sub)}."
        )
        docs.append({
            "id": f"summary-cuisine-{cuisine.lower().replace(' ', '-')}",
            "text": text,
            "metadata": {"doc_type": "summary", "section": "cuisine_summary", "cuisine": cuisine}
        })

        alc = df[df["Record_Type"] == "Alcohol"]
    if len(alc) > 0:
        bev_types = sorted(alc["Beverage_Type"].unique())
        text = (
            f"Alcohol menu summary. "
            f"Available types: {', '.join(bev_types)}. "
            f"Total alcohol items: {len(alc)}. "
            f"Served only to guests above 21 years of age; valid ID may be requested."
        )
        docs.append({
            "id": "summary-alcohol",
            "text": text,
            "metadata": {"doc_type": "summary", "section": "alcohol_summary"}
        })

    return docs

