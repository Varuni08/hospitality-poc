def parse_user_input(user_input, state, df):
    food_cuisines, beverage_types, food_categories = _get_reference_values(df)

    user_lower = user_input.lower().strip()
    prompt     = ""
    ready      = False

    if user_lower in {"reset", "start over", "restart", "back"}:
        state  = fresh_state()
        prompt = "Session reset. " + get_menu_summary(df)
        return state, False, prompt

    if state["step"] == 0:
        alcohol_keywords = {"alcohol", "drink", "beverage", "cocktail", "beer",
                            "wine", "spirits", "whisky", "whiskey", "rum", "vodka",
                            "bourbon", "martini"}
        if any(kw in user_lower for kw in alcohol_keywords):
            state["record_type"] = "Alcohol"
            state["step"]        = 1
            prompt = f"Great choice! Which type of beverage? Options: {', '.join(beverage_types)}"
        else:
            matched = _fuzzy_match(user_input, food_cuisines)
            if matched:
                state["record_type"] = "Food"
                state["cuisine"]     = matched
                state["step"]        = 2
                prompt = f"Excellent! You chose {matched} cuisine.\nWhich category? Options: {', '.join(food_categories)}"
            else:
                prompt = (
                    f"Sorry, I didn't catch that. Please choose a cuisine: "
                    f"{', '.join(food_cuisines)}\n"
                    f"or say 'alcohol' / 'drinks' for beverages."
                )

    elif state["step"] == 1 and state["record_type"] == "Alcohol":
        if user_lower in {"all", "any", "skip"}:
            state["beverage_type"] = None
        else:
            matched = _fuzzy_match(user_input, beverage_types)
            if matched:
                state["beverage_type"] = matched
            else:
                prompt = f"Please pick a beverage type: {', '.join(beverage_types)}"
                return state, False, prompt
        state["step"] = 4
        ready  = True
        prompt = "Fetching your drinks menu…"

    elif state["step"] == 2 and state["record_type"] == "Food":
        if user_lower in {"all", "any", "skip"}:
            state["category"] = None
            state["step"]     = 3
            prompt = "Got it, showing all categories. Any dietary preference? (vegetarian / non-veg / all)"
        else:
            matched = _fuzzy_match(user_input, food_categories)
            alias_map = {
                "starter": "Appetizer", "starters": "Appetizer",
                "entree":  "Main",      "entrees":  "Main",
                "mains":   "Main",
            }
            if not matched:
                matched = alias_map.get(user_lower)
            if matched:
                state["category"] = matched
                state["step"]     = 3
                prompt = f"Perfect, {matched}s it is! Any dietary preference? (vegetarian / non-veg / all)"
            else:
                prompt = f"Please choose a category: {', '.join(food_categories)} (or 'all' to skip)"

    elif state["step"] == 3:
        diet_map = {
            "veg":         "Vegetarian",
            "vegetarian":  "Vegetarian",
            "non-veg":     "Non-Vegetarian",
            "nonveg":      "Non-Vegetarian",
            "non veg":     "Non-Vegetarian",
            "vegan":       "Vegan",
            "chicken":     "Chicken",
            "seafood":     "Seafood",
            "all":         "All",
            "any":         "All",
            "skip":        "All",
        }
        matched = diet_map.get(user_lower)
        if matched:
            state["diet"] = matched
            state["step"] = 4
            ready  = True
            prompt = "Searching for the best matches…"
        else:
            prompt = "Please reply with: vegetarian, non-veg, or all."

    else:
        prompt = "Results are ready above! Type 'fresh' to start a new search."

    return state, ready, prompt

def build_filter(state):
    f = {}

    if state["record_type"] == "Food":
        f["record_type"] = "Food"
        if state.get("cuisine"):
            f["cuisine"]  = state["cuisine"]
        if state.get("category"):
            f["category"] = state["category"]
        diet = state.get("diet")
        if diet and diet != "All":
            if diet == "Vegetarian":
                f["dietary_tag"] = "Vegetarian"
            elif diet == "Non-Vegetarian":
                f["dietary_tag"] = {"$ne": "Vegetarian"}

    elif state["record_type"] == "Alcohol":
        f["record_type"] = "Alcohol"
        if state.get("beverage_type"):
            f["beverage_type"] = state["beverage_type"]

    return f


def query_items(state, index, embed_fn, top_k=50):
    if state["record_type"] == "Alcohol":
        query_text = f"alcohol {state.get('beverage_type', 'drink')} menu"
    else:
        parts = ["food menu", state.get("cuisine", ""), state.get("category", ""),
                 state.get("diet", "")]
        query_text = " ".join(p for p in parts if p and p != "All")

    query_vector = embed_fn(query_text)
    pinecone_filter = build_filter(state)

    response = index.query(
        vector    = query_vector,
        top_k     = top_k,
        filter    = pinecone_filter,
        include_metadata = True
    )

    matches = sorted(
        response.get("matches", []),
        key=lambda x: float(x["metadata"].get("rating") or 0),
        reverse=True
    )
    return matches

