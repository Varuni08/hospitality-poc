# reservation
def save_reservation(data: dict) -> str:
    res_id = f"RES{datetime.now().strftime('%y%m%d%H%M%S')}"
    supabase.table("reservations").insert({
        "id":               res_id,
        "name":             data.get("name", ""),
        "phone":            data.get("phone", ""),
        "date":             data.get("date", ""),
        "time":             data.get("time", ""),
        "guests":           data.get("guests", ""),
        "special_requests": data.get("special_requests", ""),
        "status":           "Confirmed"
    }).execute()
    vector = embedder.encode(f"{data.get('name')} {data.get('date')} {data.get('time')}").tolist()
    index.upsert(vectors=[{
        "id":       res_id,
        "values":   vector,
        "metadata": {"name": data.get("name"), "date": data.get("date"), "id": res_id}
    }])
    return res_id

def find_reservation(query: str):
    # id
    result = supabase.table("reservations").select("*").eq("id", query.upper()).execute()
    if result.data:
        return result.data[0]
    # phone
    result = supabase.table("reservations").select("*").eq("phone", query).execute()
    if result.data:
        return result.data[0]
    # name
    result = supabase.table("reservations").select("*").ilike("name", f"%{query}%").execute()
    if result.data:
        return result.data[0]
    return None

print("done")

# modify
def modify_reservation(res_id: str, field: str, value: str):
    col_map = {
        "Date":             "date",
        "Time":             "time",
        "Guests":           "guests",
        "Special Requests": "special_requests"
    }
    col = col_map.get(field)
    if col:
        supabase.table("reservations").update({col: value}).eq("id", res_id).execute()

# cancel
def cancel_reservation(res_id: str):
    supabase.table("reservations").update({"status": "Cancelled"}).eq("id", res_id).execute()

# callback req
def save_callback(data: dict) -> str:
    ref = f"CB{datetime.now().strftime('%y%m%d%H%M%S')}"
    supabase.table("callbacks").insert({
        "ref":            ref,
        "name":           data.get("name", ""),
        "phone":          data.get("phone", ""),
        "reason":         data.get("reason", ""),
        "preferred_time": data.get("preferred_time", ""),
        "status":         "Pending"
    }).execute()
    return ref

