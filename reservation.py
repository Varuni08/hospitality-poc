EXTRACT_PROMPT = """
You extract reservation data from a conversation and output ONLY raw JSON, nothing else.
No explanation, no markdown, no backticks. Just the JSON object.

Output one of these depending on the intent:
{"action": "save_reservation", "data": {"name":"","phone":"","date":"","time":"","guests":"","special_requests":""}}
{"action": "find_reservation", "data": {"query": ""}}
{"action": "modify_reservation", "data": {"field": "Date|Time|Guests|Special Requests", "value": ""}}
{"action": "cancel_reservation", "data": {}}
{"action": "save_callback", "data": {"name":"","phone":"","reason":"","preferred_time":""}}
"""

def ask_groq(messages, system=CHAT_PROMPT):
    response = groq_client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[{"role": "system", "content": system}] + messages,
        temperature=0.7,
        max_tokens=300
    )
    return response.choices[0].message.content

print("done")

def handle_reservation_action(action: dict, session: dict) -> str:
    name = action.get("action")
    data = action.get("data", {})

    if name == "save_reservation":
        res_id = save_reservation(data)
        session["last_res_id"] = res_id
        return f"[SYSTEM] Reservation saved! ID: {res_id}"

    elif name == "find_reservation":
        record = find_reservation(data.get("query", ""))
        if record:
            session["found_record"] = record
            return (f"[SYSTEM] Found — ID: {record['id']}, "
                    f"Name: {record['name']}, Date: {record['date']}, "
                    f"Time: {record['time']}, Status: {record['status']}")
        return "[SYSTEM] No reservation found."

    elif name == "modify_reservation":
        record = session.get("found_record")
        if record:
            modify_reservation(record["id"], data.get("field"), data.get("value"))
            return f"[SYSTEM] Updated {data.get('field')} to {data.get('value')}"
        return "[SYSTEM] No reservation selected."

    elif name == "cancel_reservation":
        record = session.get("found_record")
        if record:
            cancel_reservation(record["id"])
            return f"[SYSTEM] Reservation {record['id']} cancelled."
        return "[SYSTEM] No reservation selected."

    return None

print("done")

def handle_callback_action(action: dict, session: dict) -> str:
    name = action.get("action")
    data = action.get("data", {})

    if name == "save_callback":
        ref = save_callback(data)
        session["last_cb_ref"] = ref
        return f"[SYSTEM] Callback saved! Ref: {ref}"

    return None

print("done")

def strip_json(text: str) -> str:
    clean = re.sub(r'\{[^{}]*"action"[^{}]*\}', '', text, flags=re.DOTALL)
    return clean.strip()

def extract_action(messages):
    summary = "\n".join([f"{m['role']}: {m['content']}" for m in messages[-6:]])
    raw = ask_groq(
        [{"role": "user", "content": f"Extract the action from this conversation:\n{summary}"}],
        system=EXTRACT_PROMPT
    )
    try:
        clean = raw.strip().strip("```json").strip("```").strip()
        return json.loads(clean)
    except:
        return None

def chat(user_msg: str, messages: list, session: dict) -> str:
    messages.append({"role": "user", "content": user_msg})
    reply = ask_groq(messages)

    triggers = ["RESERVED", "all set", "you're booked", "reservation is confirmed", "callback"]
    triggered = any(t.lower() in reply.lower() for t in triggers)

    if triggered:
        clean_reply = strip_json(reply)
        action = extract_action(messages)
        if action:
            result = handle_reservation_action(action, session) or \
                     handle_callback_action(action, session)
            if result:
                messages.append({"role": "assistant", "content": clean_reply})
                messages.append({"role": "user", "content": result})
                final_reply = strip_json(ask_groq(messages))
                messages.append({"role": "assistant", "content": final_reply})
                return final_reply
        messages.append({"role": "assistant", "content": clean_reply})
        return clean_reply

    messages.append({"role": "assistant", "content": strip_json(reply)})
    return strip_json(reply)

print("done")

